# include <iostream>
using namespace std;

int main () 
{
  char a = '~';
  char b = '&';
  char c = '%';
  char d = 'A';

  cout << "ASCII Value of ~ is: " << int(a) << "\n";
  cout << "ASCII Value of & is: " << int(b) << "\n";
  cout << "ASCII Value of % is: " << int(c) << "\n";
  cout << "ASCII Value of A is: " << int(d) << "\n";

  return 0;
}